public class Mult {

    public static int multiplication(int a, int b)
    {
         int product = a * b;
         return product;

    }
    public static void main(String args [])
    {
        int a = 3;
        int b = 5;
        int prod = multiplication(a,b);

        System.out.println("A *  B = " + prod);
    }
}
